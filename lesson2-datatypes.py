# отсутствие данных / NoneType
a = None
print(type(a))

# целое число / int
a = 1
print(type(a))

# число с плавающей точкой / float
a = 1.0
print(type(a))

# комплексное число / complex
a = 1 + 1j
print(type(a))

# строка / str
a = '1'
print(type(a))

# список / list
a = [1, 1, 'a']
print(type(a))

# кортеж / tuple
a = (1, 1, 'a')
print(type(a))

# множество / set
a = {1, 1, 'a'}
print(type(a))

# словарь / dict
a = {'a': 1, 'b': 2}
print(type(a))

# логическое / bool
a = True
print(type(a))